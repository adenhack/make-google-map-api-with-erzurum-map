var ilce = document.querySelector("#ilceler");

function uzundere_mahalleler(e) {
    if (ilce.value === "uzundere" && mahalle.value === "BALIKLI") {
        uzunderebalıklı();
    } else if (ilce.value === "uzundere" && mahalle.value === "ÇAMLIYAMAÇ") {
        uzundereçamlıyamaç();
    } else if (ilce.value === "uzundere" && mahalle.value === "KİRAZLI") {
        uzunderekirazlı();
    } else if (ilce.value === "uzundere" && mahalle.value === "ALTINÇANAK") {
        uzunderealtınçanak();
    } else if (ilce.value === "uzundere" && mahalle.value === "ÇAĞLAYAN") {
        uzundereçağlayanlı();
    } else if (ilce.value === "uzundere" && mahalle.value === "CEVİZLİ") {
        uzunderecevizli();
    } else if (ilce.value === "uzundere" && mahalle.value === "DİKYAR") {
        uzunderedikyar();
    } else if (ilce.value === "uzundere" && mahalle.value === "GÖLBAŞI") {
        uzunderegölbaşı();
    } else if (ilce.value === "uzundere" && mahalle.value === "ULUBAĞ") {
        uzundereulubağ();
    } else if (ilce.value === "uzundere" && mahalle.value === "SAPACA") {
        uzunderesapaca();
    } else if (ilce.value === "uzundere" && mahalle.value === "CÖMERTLER") {
        uzunderecömertler();
    } else if (ilce.value === "uzundere" && mahalle.value === "ÇAYBAŞI") {
        uzundereçaybaşı();
    } else if (ilce.value === "uzundere" && mahalle.value === "ERİKLİ") {
        uzundereerikli();
    } else if (ilce.value === "uzundere" && mahalle.value === "MERKEZ") {
        uzunderemerkez();
    } else if (ilce.value === "uzundere" && mahalle.value === "MURATEFENDİ") {
        uzunderemuratefendi();
    } else if (ilce.value === "uzundere" && mahalle.value === "SEYİTEFENDİ") {
        uzundereseyitefendi();
    } else if (ilce.value === "uzundere" && mahalle.value === "UZUNDERE") {
        uzunderemahalle();
    } else if (ilce.value === "uzundere" && mahalle.value === "YAYLA") {
        uzundereyayla();
    }
}